//  What is Camel Casing?
/*
    So, basically Camel Casing means where we write string in such a way that gives it an
    up-down look. Like a camel has for eg, showMyData.
    
*/  

// What is Snake Casing?
/*
    So, basically Snake Casing means where we write string along with underscores. 
    for eg, show_My_Data.
*/ 

// ********************* BASIC POINTS TO KEEP REMEMBER IN JAVA ****************
/*
    Use Case:-

    1. class and interface :- Use first character as capital letter.
    for eg, Human, Students.

    2. variables and methods:- Write in a running text format.
    for eg, marks, data.

    3. Constants:- Write all the characters in capitla letter.
    for eg, PIE.

    4. Constructor:- Use brackets after writing the name. And always use class name as the 
    constructor.
    for eg, Human(), Students().
*/

